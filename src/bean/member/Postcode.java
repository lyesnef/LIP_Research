package bean.member;

public class Postcode {

	private int postcodeNumber;
	private String postcode1st;
	private String postcode2nd;
	private String postcodeAddress;
	
	public String getPostcode1st() {
		return postcode1st;
	}
	public void setPostcode1st(String postcode1st) {
		this.postcode1st = postcode1st;
	}
	public String getPostcode2nd() {
		return postcode2nd;
	}
	public void setPostcode2nd(String postcode2nd) {
		this.postcode2nd = postcode2nd;
	}
	public int getPostcodeNumber() {
		return postcodeNumber;
	}
	public void setPostcodeNumber(int postcodeNumber) {
		this.postcodeNumber = postcodeNumber;
	}
	public String getPostcodeAddress() {
		return postcodeAddress;
	}
	public void setPostcodeAddress(String postcodeAddress) {
		this.postcodeAddress = postcodeAddress;
	}
	
}
