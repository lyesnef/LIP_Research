package bean.member;

public class Gender {
	private int genderNumber;
	private String genderName;
	private int genderCounter;
	public int getGenderNumber() {
		return genderNumber;
	}
	public void setGenderNumber(int genderNumber) {
		this.genderNumber = genderNumber;
	}
	public String getGenderName() {
		return genderName;
	}
	public void setGenderName(String genderName) {
		this.genderName = genderName;
	}
	public int getGenderCounter() {
		return genderCounter;
	}
	public void setGenderCounter(int genderCounter) {
		this.genderCounter = genderCounter;
	}
	
	
}
