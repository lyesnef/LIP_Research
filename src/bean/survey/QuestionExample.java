package bean.survey;

public class QuestionExample {

	private int questionExampleNumber;
	private int questionNumber;
	private int exampleNumber;

	public int getQuestionExampleNumber() {
		return questionExampleNumber;
	}
	public void setQuestionExampleNumber(int questionExampleNumber) {
		this.questionExampleNumber = questionExampleNumber;
	}
	public int getQuestionNumber() {
		return questionNumber;
	}
	public void setQuestionNumber(int questionNumber) {
		this.questionNumber = questionNumber;
	}
	public int getExampleNumber() {
		return exampleNumber;
	}
	public void setExampleNumber(int exampleNumber) {
		this.exampleNumber = exampleNumber;
	}
}
