package bean.point;

import java.util.Date;

public class pointOutcome {

	private int pointOutcomeNumber;
	private Date pointOutcomeDate;
	private int pointOutcomePoint;
	private String pointOutcomeDetail;
	private String pointOutcomeList;
	private int memberNumber;
	
	public int getPointOutcomeNumber() {
		return pointOutcomeNumber;
	}
	public void setPointOutcomeNumber(int pointOutcomeNumber) {
		this.pointOutcomeNumber = pointOutcomeNumber;
	}
	public Date getPointOutcomeDate() {
		return pointOutcomeDate;
	}
	public void setPointOutcomeDate(Date pointOutcomeDate) {
		this.pointOutcomeDate = pointOutcomeDate;
	}
	public int getPointOutcomePoint() {
		return pointOutcomePoint;
	}
	public void setPointOutcomePoint(int pointOutcomePoint) {
		this.pointOutcomePoint = pointOutcomePoint;
	}
	public String getPointOutcomeDetail() {
		return pointOutcomeDetail;
	}
	public void setPointOutcomeDetail(String pointOutcomeDetail) {
		this.pointOutcomeDetail = pointOutcomeDetail;
	}
	public String getPointOutcomeList() {
		return pointOutcomeList;
	}
	public void setPointOutcomeList(String pointOutcomeList) {
		this.pointOutcomeList = pointOutcomeList;
	}
	public int getMemberNumber() {
		return memberNumber;
	}
	public void setMemberNumber(int memberNumber) {
		this.memberNumber = memberNumber;
	}
	
}
