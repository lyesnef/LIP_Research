package bean.etc;

import java.util.Date;

public class MemberLog {

	private int memberLogNumber;
	private String memberLogContant;
	private Date memberLogDate;
	private String memberLogIp;
	private int memberLogFrom;
	private int memberNumber;
	
	public int getMemberLogNumber() {
		return memberLogNumber;
	}
	public void setMemberLogNumber(int memberLogNumber) {
		this.memberLogNumber = memberLogNumber;
	}
	public String getMemberLogContant() {
		return memberLogContant;
	}
	public void setMemberLogContant(String memberLogContant) {
		this.memberLogContant = memberLogContant;
	}
	public Date getMemberLogDate() {
		return memberLogDate;
	}
	public void setMemberLogDate(Date memberLogDate) {
		this.memberLogDate = memberLogDate;
	}
	public String getMemberLogIp() {
		return memberLogIp;
	}
	public void setMemberLogIp(String memberLogIp) {
		this.memberLogIp = memberLogIp;
	}
	public int getMemberLogFrom() {
		return memberLogFrom;
	}
	public void setMemberLogFrom(int memberLogFrom) {
		this.memberLogFrom = memberLogFrom;
	}
	public int getMemberNumber() {
		return memberNumber;
	}
	public void setMemberNumber(int memberNumber) {
		this.memberNumber = memberNumber;
	}
	
}
